void afxUITask(void *pvParameters)
{
    static uint32_t rightUpdateTick = 0;
    
    lcdDisplayInit();
    
	for ( ;; )
    {
        yield(200);
		
	if (sensorData.overload[0] + sensorData.overload[1] + sensorData.overload[2])
    {
        uiData.warnModeRun = 1;
    }
		
		/* 参数变动刷新 */
		if (uiData.leftMode == UI_LEFT_AUTO) 
		{
			if (autoLastSpeed != motorData.setSpeedLevel)
			{
				autoLastSpeed = motorData.setSpeedLevel;
				uiData.leftUpdateReq = 1;
			}
		}

        /* 全局更新 */
        if (uiData.leftDisplay)
        {
					if((uwTick - encoderData.leftUpdateTick) >= 500)
					{
            if (leftLast != uiData.leftMode)
            {
                leftLast = uiData.leftMode;
                
                leftLcdChange(uiData.leftMode);
            }
            else
            {
                if (uiData.leftUpdateReq == 1)
                {
                    uiData.leftUpdateReq = 0;
                    leftLcdUpdate(uiData.leftMode);
                }
            }
					}
        }
        else
        {
            leftLcdOff();
        }
        
        /* 全屏更新 */
        if (uiData.rightDisplay)
        {
            if (uiData.warnModeRun)
            {
                if (sensorData.overload[0] + sensorData.overload[1] + sensorData.overload[2])
                {
                    if (uwTick - rightUpdateTick > 3000)
                    {
                        rightUpdateTick = uwTick;
                        
                        /* PM2.5/甲醛/滤网 */
                        if (uiData.rightMode == UI_RIGHT_AQI)
                        {
                            if (sensorData.overload[0] == 1)
                            {
                                uiData.rightMode = UI_RIGHT_PM25;
                            }
                            else if (sensorData.overload[1] == 1)
                            {
                                uiData.rightMode = UI_RIGHT_METHANAL;
                            }
                            else if (sensorData.overload[2] == 1)
                            {
                                uiData.rightMode = UI_RIGHT_FILTER;
                            }
                        }
                        else if (uiData.rightMode == UI_RIGHT_PM25)
                        {
                            if (sensorData.overload[1] == 1)
                            {
                                uiData.rightMode = UI_RIGHT_METHANAL;
                            }
                            else if (sensorData.overload[2] == 1)
                            {
                                uiData.rightMode = UI_RIGHT_FILTER;
                            }
                        }
                        else if (uiData.rightMode == UI_RIGHT_METHANAL)
                        {
                            if (sensorData.overload[2] == 1)
                            {
                                uiData.rightMode = UI_RIGHT_FILTER;
                            }
                            else if (sensorData.overload[0] == 1)
                            {
                                uiData.rightMode = UI_RIGHT_PM25;
                            }
                        }
                        else if (uiData.rightMode == UI_RIGHT_FILTER)
                        {
                            if (sensorData.overload[0] == 1)
                            {
                                uiData.rightMode = UI_RIGHT_PM25;
                            }
                            else if (sensorData.overload[1] == 1)
                            {
                                uiData.rightMode = UI_RIGHT_METHANAL;
                            }
                        }
                        
                        rightLcdChange(uiData.rightMode);
                    }
                }
                else
                {
                    uiData.warnModeRun = 0;
                }
            }
            else
            {
							if(uwTick - encoderData.rightUpdateTick > 500)//500
							{
                if (rightLast != uiData.rightMode)
                {
											rightLcdChange(uiData.rightMode);
											rightLast = uiData.rightMode;
											rightUpdateTick = uwTick + 1000;
                }
                else
                {
										if(uwTick > rightUpdateTick)
										{
											if (uwTick - rightUpdateTick > 1000)  //1000
											{
													rightUpdateTick = uwTick;
													rightLcdUpdate(uiData.rightMode);
											}
										}
                }
							}
            }
        }
        else
        {
            rightLcdOff();
        }
	}
}

